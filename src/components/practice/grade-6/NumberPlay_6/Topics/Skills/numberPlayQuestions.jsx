// SKILL 5: Kaprekar's Constant
// ─────────────────────────────────────────────────────────────
export const generateKaprekarQuestions = () => {
    let qs = [];

    // Type 1: Sort Descending
    for (let i = 0; i < 7; i++) {
        let digits = [rnd(1,9), rnd(1,9), rnd(0,9), rnd(0,9)];
        digits.sort(() => Math.random() - 0.5); // shuffle original
        let numStr = digits.join('');
        
        let desc = [...digits].sort((a,b) => b-a).join('');
        let asc = [...digits].sort((a,b) => a-b).join('');
        
        qs.push({
            type: 'text',
            question: `Step 1 of the Kaprekar routine for the number **${numStr}** is to arrange the digits in **descending** order. What is the exact resulting number?`,
            answer: `${desc}`,
            explanation: `Sort from highest to lowest: **${desc}**.`
        });
    }

    // Type 2: Full step execution
    for (let i = 0; i < 7; i++) {
        let d1 = rnd(5, 9);
        let d2 = rnd(4, d1-1);
        let d3 = rnd(2, d2-1);
        let d4 = rnd(0, d3-1);
        
        let descStr = `${d1}${d2}${d3}${d4}`;
        let ascStr = `${d4}${d3}${d2}${d1}`;
        let res = parseInt(descStr) - parseInt(ascStr);

        // Mix digits for the prompt
        let numStr = shuffle([d1,d2,d3,d4]).join('');

        qs.push({
            type: 'mcq',
            question: `Execute one full Kaprekar step (Descending - Ascending) for the number **${numStr}**. What is the result?`,
            options: generateDistinctOptions(res, res - 300, res + 300).map(String),
            correct: `${res}`,
            explanation: `Descending: $${descStr}$. Ascending: $${ascStr}$. \n $${descStr} - ${ascStr} = \mathbf{${res}}$.`
        });
    }

    // Type 3: Rules and padding
    for (let i = 0; i < 6; i++) {
        qs.push({
            type: 'mcq',
            question: `During the routine, if your subtraction results in the 3-digit number **999**, how must you write it for the next step?`,
            options: ['0999', '9990', 'Wait, you stop here.', 'Multiply by 10'],
            correct: '0999',
            explanation: `Kaprekar's routine for 4 digits strictly requires 4 digits. You must pad it with a leading zero to make it **0999**.`
        });
    }

    // Since Type 3 only has 1 unique prompt logically, I'll repeat variants or just let shuffle handle it.

    return shuffle(qs).slice(0, 20);
};

