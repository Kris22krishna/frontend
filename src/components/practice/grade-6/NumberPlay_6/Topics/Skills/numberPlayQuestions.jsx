// SKILL 1: Numbers in Daily Life
// ─────────────────────────────────────────────────────────────
export const generateNumbersDailyLifeQuestions = () => {
    let qs = [];
    
    // Type 1: Place value combinations
    for (let i = 0; i < 7; i++) {
        let h, t, o;
        do {
            h = rnd(2, 9);
            t = rnd(1, 9);
            o = rnd(1, 9);
        } while (h === t || t === o || h === o);
        let num = h * 100 + t * 10 + o;
        qs.push({
            type: 'mcq',
            question: `In the number $${num}$, what is the exact place value of the digit **${t}**?`,
            options: shuffle([`${t}`, `${t}0`, `${t}00`, `${t}000`]),
            correct: `${t}0`,
            explanation: `The digit ${t} is in the tens place, so its value is ${t} \\times 10 = ${t}0.`
        });
    }

    // Type 2: Expanded form
    for (let i = 0; i < 7; i++) {
        let th = rnd(1, 9);
        let h = rnd(1, 9);
        let t = rnd(1, 9);
        let o = rnd(1, 9);
        let num = th * 1000 + h * 100 + t * 10 + o;
        qs.push({
            type: 'text',
            question: `Write the standard form of the number: $(${th} \\times 1000) + (${h} \\times 100) + (${t} \\times 10) + ${o}$`,
            answer: `${num}`,
            explanation: `Simply add them up: $${th}000 + ${h}00 + ${t}0 + ${o} = ${num}$.`
        });
    }

    // Type 3: Missing digits in codes
    for (let i = 0; i < 6; i++) {
        let code = Array.from({length: 4}, () => rnd(1,9));
        let sum = code.reduce((a, b) => a + b, 0);
        let missingIdx = rnd(0, 3);
        let missing = code[missingIdx];
        let displayCode = [...code];
        displayCode[missingIdx] = 'X';
        
        qs.push({
            type: 'mcq',
            question: `A secret 4-digit PIN is **${displayCode.join('')}**. You are told the sum of all four digits is exactly **${sum}**. What is the value of **X**?`,
            options: generateDistinctOptions(missing, 1, 9).map(String),
            correct: String(missing),
            explanation: `The known digits sum to ${sum - missing}. So, $${sum} - ${sum - missing} = ${missing}$.`
        });
    }

    return shuffle(qs).slice(0, 20);
};

