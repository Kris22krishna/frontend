// SKILL 2: Patterns on the Number Line
// ─────────────────────────────────────────────────────────────
export const generateNumberLinePatternsQuestions = () => {
    let qs = [];
    
    // Type 1: Arithmetic missing term
    for (let i = 0; i < 7; i++) {
        let start = rnd(2, 20);
        let step = rnd(3, 12);
        let seq = [start, start + step, start + 2*step, start + 3*step, start + 4*step];
        let missingIdx = rnd(1, 3); // don't hide first or last
        let missing = seq[missingIdx];
        seq[missingIdx] = "?";
        
        qs.push({
            type: 'mcq',
            question: `Identify the missing number in the sequence: **${seq.join(', ')}**`,
            options: generateDistinctOptions(missing, missing - 10, missing + 15).map(String),
            correct: String(missing),
            explanation: `The pattern increases by $+${step}$ each time. So, $${seq[missingIdx-1]} + ${step} = ${missing}$.`
        });
    }

    // Type 2: Geometric missing term
    for (let i = 0; i < 7; i++) {
        let start = rnd(2, 5);
        let mult = rnd(2, 4);
        let seq = [start, start * mult, start * mult**2, start * mult**3, start * mult**4];
        let missingIdx = rnd(2, 3);
        let missing = seq[missingIdx];
        seq[missingIdx] = "?";
        
        qs.push({
            type: 'text',
            question: `Find the secret multiplier, then type the missing number: **${seq.join(', ')}**`,
            answer: `${missing}`,
            explanation: `Each term is multiplied by $\\times${mult}$. So $${seq[missingIdx-1]} \\times ${mult} = ${missing}$.`
        });
    }

    // Type 3: Find the Nth term logic
    for (let i = 0; i < 6; i++) {
        let step = rnd(2, 6);
        let base = rnd(1, 5);
        qs.push({
            type: 'mcq',
            question: `A sequence is generated by the rule: "Multiply position by ${step}, then add ${base}". What is the value of the 5th number in this sequence?`,
            options: generateDistinctOptions((5 * step) + base, 10, 40).map(String),
            correct: String((5 * step) + base),
            explanation: `Apply the rule to position $5$: $(5 \\times ${step}) + ${base} = ${(5 * step) + base}$.`
        });
    }

    return shuffle(qs).slice(0, 20);
};

