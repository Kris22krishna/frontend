// SKILL 3: Supercells (Number Puzzles)
// ─────────────────────────────────────────────────────────────
export const generateSupercellsQuestions = () => {
    let qs = [];
    
    // Type 1: Magic Square missing
    for (let i = 0; i < 7; i++) {
        let ms = [8,1,6, 3,5,7, 4,9,2]; // base magic square
        let add = rnd(1, 10); // scale it!
        ms = ms.map(x => x + add);
        let sum = (15) + (3*add); // magic sum
        
        let hideIdx = rnd(0, 8);
        let missing = ms[hideIdx];
        
        // build an SVG grid representation
        let cells = "";
        for(let r=0; r<3; r++) {
            for(let c=0; c<3; c++) {
                let idx = r*3 + c;
                let val = idx === hideIdx ? "?" : ms[idx];
                cells += `<rect x="${c*40}" y="${r*40}" width="40" height="40" fill="#fff" stroke="#f59e0b" stroke-width="2"/>`;
                cells += `<text x="${c*40 + 20}" y="${r*40 + 25}" font-size="16" font-weight="bold" fill="${idx === hideIdx ? '#ef4444' : '#0f172a'}" text-anchor="middle">${val}</text>`;
            }
        }
        
        let svg = `<svg width="120" height="120" viewBox="-2 -2 124 124" xmlns="http://www.w3.org/2000/svg">${cells}</svg>`;

        qs.push({
            type: 'mcq',
            question: `In this Magic Square, every row, column, and diagonal adds up to **${sum}**. What is the value of the missing cell '?'`,
            svg: svg,
            options: generateDistinctOptions(missing, missing - 5, missing + 5).map(String),
            correct: String(missing),
            explanation: `Look at a completed row or column involving the '?'. The missing value is $${sum}$ minus the other known values, which is **${missing}**.`
        });
    }

    // Type 2: KenKen-style supercell math (+/-)
    for (let i = 0; i < 7; i++) {
        let a = rnd(2, 9);
        let b = rnd(1, a-1); // ensure distinct
        let target = a + b;
        qs.push({
            type: 'mcq',
            question: `A puzzle "Supercell" has exactly two boxes and a clue "**${target}+**". This means the two digits inside must add up exactly to ${target}. Which pair of digits could NOT be in this supercell if you can only use digits 1-9 without repeating?`,
            options: [
                `${a} and ${b}`,
                `${target-1} and 1` !== `${a} and ${b}` ? `${target-1} and 1` : `${target-2} and 2`,
                `${target} and 0`, // correct answer, 0 not allowed
                `${b} and ${a}` // duplicate valid 
            ].filter((v,i,a)=>a.indexOf(v)===i), // unique hack
            correct: `${target} and 0`,
            explanation: `Digits 1-9 are allowed. You cannot use 0, so **${target} and 0** is invalid.`
        });
        // cleanup options format to guarantee 4 distinct choices
        let currentQs = qs[qs.length-1];
        let pool = [`${target} and 0`, `${target+1} and -1`, `0 and ${target}`, `${target-1} and 1`, `${target-2} and 2`, `${target-3} and 3`];
        currentQs.options = shuffle([pool[0], pool[3], pool[4], pool[5]].slice(0,4));
        currentQs.correct = pool[0];
    }

    // Type 3: Multiplication cages
    for (let i = 0; i < 6; i++) {
        let f1 = rnd(2, 6);
        let f2 = rnd(3, 7);
        let prod = f1 * f2;
        qs.push({
            type: 'text',
            question: `A Supercell block spans exactly 2 squares and has the clue "**${prod}x**". If one of the numbers is **${f1}**, what MUST the other number be?`,
            answer: `${f2}`,
            explanation: `The clue means the numbers multiply to ${prod}. Since ${f1} $\\times$ X = ${prod}$, the other number is **${f2}**.`
        });
    }

    return shuffle(qs).slice(0, 20);
};

