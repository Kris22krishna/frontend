export const generatePalindromicQuestions = () => {
    let qs = [];

    // Type 1: Identify Palindrome
    for (let i = 0; i < 7; i++) {
        let a = rnd(1, 9);
        let b = rnd(0, 9);
        let c = rnd(0, 9);
        
        let pal = `${a}${b}${c}${b}${a}`;
        let non1 = `${a}${b}${c}${a}${b}`;
        let non2 = `${b}${a}${c}${b}${a}`;
        let non3 = `${a}${c}${b}${a}${c}`;
        
        let opts = shuffle([pal, non1, non2, non3]);
        qs.push({
            type: 'mcq',
            question: `Which of the following $5$-digit numbers is a true **Palindrome**?`,
            options: opts,
            correct: pal,
            explanation: `**${pal}** reads exactly the same from left-to-right as it does right-to-left.`
        });
    }

    // Type 2: Reverse and Add (1 Step)
    for (let i = 0; i < 7; i++) {
        let t = rnd(2, 6);
        let o = rnd(1, 5); // keeping logic simple to avoid carry
        let num = t * 10 + o;
        let rev = o * 10 + t;
        let pResult = num + rev;

        qs.push({
            type: 'text',
            question: `Apply the "Reverse and Add" rule to the number **${num}**. What palindromic number do you get after one step?`,
            answer: `${pResult}`,
            explanation: `Reverse of ${num} is ${rev}. $${num} + ${rev} = \mathbf{${pResult}}$.`
        });
    }

    // Type 3: Fill in missing digit
    for (let i = 0; i < 6; i++) {
        let a = rnd(1, 9);
        let b = rnd(0, 9);
        let d = rnd(0, 9);
        qs.push({
            type: 'mcq',
            question: `If the 5-digit number **${a}${b}X${d}${a}** is a true palindrome, what digit MUST take the place of X?`,
            options: [`${b}`, `${a}`, `${d}`, `Any digit 0-9`],
            correct: `Any digit 0-9`,
            explanation: `For the number to be palindromic, the outer pairs must match ($a=a$, $b=d$). If $b=d$, then X is exactly in the center and can be **Any digit 0-9**! Wait, actually if $b \\neq d$ the question is broken. Let me fix the logic.`
        });
        
        // Correct logic:
        let qs1 = qs.pop();
        qs.push({
            type: 'mcq',
            question: `The 4-digit number **${a}${b}X${a}** is a palindrome. What is the value of **X**?`,
            options: generateDistinctOptions(b, 0, 9).map(String),
            correct: `${b}`,
            explanation: `In a 4-digit palindrome ($ABBA$), the inner 2 digits must be identical. So $X = ${b}$.`
        });
    }

    return shuffle(qs).slice(0, 20);
};

