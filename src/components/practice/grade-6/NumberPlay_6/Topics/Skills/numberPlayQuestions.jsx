// SKILL 6: Number Games & Strategies
// ─────────────────────────────────────────────────────────────
export const generateNumberGamesQuestions = () => {
    let qs = [];

    // Type 1: Digital Root
    for (let i = 0; i < 10; i++) {
        let a = rnd(2, 9);
        let b = rnd(1, 9);
        let c = rnd(1, 9);
        
        let sum1 = a+b+c;
        let dr = sum1;
        while(dr > 9) {
            let sStr = String(dr);
            let t = 0;
            for(let j=0; j<sStr.length; j++) t += parseInt(sStr[j]);
            dr = t;
        }

        qs.push({
            type: 'text',
            question: `Calculate the completely condensed **Digital Root** of **${a}${b}${c}**. (Sum digits repeatedly until 1 digit is left)`,
            answer: `${dr}`,
            explanation: `$${a}+${b}+${c} = ${sum1}$. Then keep adding those digits until you get exactly **${dr}**.`
        });
    }

    // Type 2: Parity strategy
    for (let i = 0; i < 10; i++) {
        let tokens = rnd(11, 25);
        let pool = tokens % 2 !== 0 ? 'Odd' : 'Even';
        
        qs.push({
            type: 'mcq',
            question: `You are playing Nim with **${tokens}** tokens. You can take 1 or 2. If you want to leave an **Even** amount for your opponent to start their turn, how many tokens must you take on your very first move?`,
            options: ['Take 1', 'Take 2', 'Take 3', 'Take 4'].slice(0, 2), // only 1 or 2 allowed
            correct: tokens % 2 === 0 ? 'Take 2' : 'Take 1',
            explanation: `The current total is ${tokens} (${pool}). To leave an Even number, you must subtract ${tokens % 2 === 0 ? '2 (Even - Even = Even)' : '1 (Odd - Odd = Even)'}.`
        });
    }

    // Make options 4 items for MCQ compatibility
    qs.forEach(q => {
        if(q.type === 'mcq' && q.options.length === 2) {
             q.options = ['Take 1', 'Take 2', 'Take none', 'Skip turn'];
             shuffle(q.options);
        }
    });

    return shuffle(qs).slice(0, 20);
};
